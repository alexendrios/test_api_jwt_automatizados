package payloads;

public class Payload {
    private String Role;
    private String Seed;
    private String Name;

    public String getRole() {
        return Role;
    }

    public void setRole(String role) {
        Role = role;
    }

    public String getSeed() {
        return Seed;
    }

    public void setSeed(String seed) {
        Seed = seed;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

}
